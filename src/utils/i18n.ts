export type TranslationKey = keyof typeof translations.en;

type TranslationParams = Record<string, string | number>;

export function translate(language: Language, key: TranslationKey, params: TranslationParams = {}) {
  const template = translations[language][key] ?? translations.en[key];

  return template.replace(/\{(\w+)\}/g, (_, token: string) => String(params[token] ?? `{${token}}`));
}

export function getLocale(language: Language) {
  return language === "ru" ? "ru-RU" : "en-US";
}
